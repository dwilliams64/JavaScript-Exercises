let myStr = "Chris Redfield";

// Finds length of string

console.log(myStr.length); // 14

// You can access the individual characters of a string
// by using the bracket notation similar to arrays

console.log(myStr[0]); // "C"

// Subtracts the length of the the string by
// 2. The number we get from the length of the 
// string is 14 minus the 2 is 12 which will
// be "l" or the second to last character of string

console.log(myStr[myStr.length - 2]); // "l"