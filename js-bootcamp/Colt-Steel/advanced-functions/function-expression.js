// Function Statement
function add(x, y) {
    return x + y;
}

// Function Expression (Anonymous)
const sum = function (x, y) {
  return x + y;
}

// Function Expression (Named)
const product = function multiply(x, y) {
  return x * y;
}

// Note: Functions are objects in JavaScript.

